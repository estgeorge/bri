def BRIB(k,i,j):
    if k > 2:
        A = BRIA(k-1,i,j)
        B = BRIB(k-1,i,j+1)        
        C = BRIC(k-1,i+1,j)        
        D = BRID(k-1,i+1,j+1) 
    else:
        A = getBlock(i,j)
        B = getBlock(i,j+1)
        C = getBlock(i+1,j)
        D = getBlock(i+1,j+1)  
    return B-A@inv(C)@D

def BRIC(k,i,j):
    if k > 2:
        A = BRIA(k-1,i,j)
        B = BRIB(k-1,i,j+1)        
        C = BRIC(k-1,i+1,j)        
        D = BRID(k-1,i+1,j+1)    
    else:
        A = getBlock(i,j)
        B = getBlock(i,j+1)
        C = getBlock(i+1,j)
        D = getBlock(i+1,j+1)  
    return C-D@inv(B)@A

def BRID(k,i,j):
    if k > 2:
        A = BRIA(k-1,i,j)
        B = BRIB(k-1,i,j+1)        
        C = BRIC(k-1,i+1,j)        
        D = BRID(k-1,i+1,j+1)        
    else:
        A = getBlock(i,j)
        B = getBlock(i,j+1)
        C = getBlock(i+1,j)
        D = getBlock(i+1,j+1)  
    return D-C@inv(A)@B


